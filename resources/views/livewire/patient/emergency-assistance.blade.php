    <script>
        function shareLocation() {
            if (navigator.geolocation) {
                navigator.geolocation.getCurrentPosition(function(position) {
                    const lat = position.coords.latitude;
                    const lng = position.coords.longitude;
                    const locationUrl = `https://www.google.com/maps?q=${lat},${lng}`;
                    
                    // Copy to clipboard
                    navigator.clipboard.writeText(locationUrl).then(function() {
                        alert(`Location copied to clipboard!\nLatitude: ${lat}\nLongitude: ${lng}\n\nYou can share this with emergency services.`);
                    }).catch(function() {
                        alert(`Your Location:\nLatitude: ${lat}\nLongitude: ${lng}\n\nMap: ${locationUrl}`);
                    });
                }, function(error) {
                    alert('Unable to get your location. Please enable location services.');
                });
            } else {
                alert('Geolocation is not supported by your browser.');
            }
        }

        function findNearestHospital() {
            if (navigator.geolocation) {
                navigator.geolocation.getCurrentPosition(function(position) {
                    const lat = position.coords.latitude;
                    const lng = position.coords.longitude;
                    const searchUrl = `https://www.google.com/maps/search/hospital/@${lat},${lng},15z`;
                    window.open(searchUrl, '_blank');
                }, function(error) {
                    alert('Unable to get your location. Please enable location services.');
                });
            } else {
                alert('Geolocation is not supported by your browser.');
            }
        }

